#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*addr_of_next_el;

	while (*lst)
	{
		addr_of_next_el = (*lst)->next;
		del((*lst)->content);
		free(*lst);
		*lst = addr_of_next_el;
	}
	*lst = NULL;
}
